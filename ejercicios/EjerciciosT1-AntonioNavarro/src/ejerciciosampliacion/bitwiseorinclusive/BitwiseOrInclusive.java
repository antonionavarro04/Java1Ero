package bitwiseorinclusive;

public class BitwiseOrInclusive {

	public static void main(String[] args) {
		byte c = 6;
		byte d = 6;
		
		System.out.println(c == 6 || c++ == 7);
		System.out.println(c);
		System.out.println();
		System.out.println(d > 7 | d++ == 7);
		System.out.println(d);

	}

}
